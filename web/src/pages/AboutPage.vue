  <h1>About</h1>
  TV Tracker is a locally hosted tracker of television you are watching.

  It's purpose is to be able to show you when new episodes are avialble and it will indicate which service you own that is showing the episode.

  At this time everything is manual!  You need to:

  - Add the show/movie you want to track
  - Specify what streaming service you have that has that show.
  - Mark episodes that you have watch as "Done".

